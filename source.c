uint16_t get_temp_spi(uint8_t *temp_data_spi){
	uint16_t temp;
	HAL_GPIO_WritePin(CS_GPIO_Port, CS_Pin, GPIO_PIN_RESET);
	HAL_Delay(1);
	HAL_SPI_Receive(&hspi1, temp_data_spi, 1, -1);
	HAL_GPIO_WritePin(CS_GPIO_Port, CS_Pin, GPIO_PIN_SET);

	temp = temp_data_spi[1];
	temp = temp << 8;
	temp = temp + temp_data_spi[0];
	temp = temp >> 3;
	temp /= 4;

	return temp;
}

uint16_t receive_max_temp(uint8_t *rx_data){
	uint16_t max_temp;
	uint8_t enter_str[] = "Enter max temperature: ";

	HAL_UART_Transmit(&huart1, enter_str, sizeof(enter_str), -1);
	HAL_UART_Receive(&huart1, rx_data, 3, -1);
	HAL_UART_Transmit(&huart1, rx_data, sizeof(rx_data), -1);
	HAL_UART_Transmit(&huart1, (uint8_t*)"\r\n", 2, -1);
	max_temp = (rx_data[0] - 48) * 100 + (rx_data[1] - 48) * 10 + (rx_data[2] - 48);

	return max_temp;

}

